#!/bin/bash

# 数据行数组
data=(
  "4,4,512,1"
  "4,4,64,8"
  "4,4,16,16"
  "4,4,8,64"
  "4,4,1,512"
  "8,8,512,1"
  "8,8,64,8"
  "8,8,16,16"
  "8,8,8,64"
  "8,8,1,512"
  "16,16,512,1"
  "16,16,64,8"
  "16,16,16,16"
  "16,16,8,64"
  "16,16,1,51"
)

# 遍历数据行
for line in "${data[@]}"; do
  # 用逗号分割行
  IFS=',' read -r -a params <<< "$line"
  
  # 获取参数
  g1=${params[0]}
  g2=${params[1]}
  b1=${params[2]}
  b2=${params[3]}
  
  # 构建命令
  cmd="./testAdvect -g ${g1},${g2} -b ${b1},${b2} 4096 4096 100"
  
  # 输出命令
  $cmd
done

